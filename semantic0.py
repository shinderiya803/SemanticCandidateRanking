#Skill Score
core_skills = ["embedding","retrieval","ranking","llm","fine-tuning","faiss","milvus",
               "qdrant","weaviate","pinecone","python",  "lora","peft", "search"]

def skill_score(candidate):

    skills = [
        skill['name'].lower()
        for skill in candidate['skills']
    ]

    matches = sum(
        keyword in " ".join(skills)
        for keyword in core_skills
    )

    return matches / len(core_skills)

#Experience Score
def experience_score(candidate):

    years = candidate['profile']['years_of_experience']

    if 5 <= years <= 9:
        return 1.0

    elif 4 <= years < 5:
        return 0.8

    elif 9 < years <= 10:
        return 0.8

    else:
        return 0.4

#redrobe Score
def redrob_score(candidate):

    signals = candidate['redrob_signals']

    score = 0

    if signals['open_to_work_flag']:
        score += 0.2

    score += (
        signals['recruiter_response_rate']
    ) * 0.2

    score += (
        signals['interview_completion_rate']
    ) * 0.2

    score += (
        signals['offer_acceptance_rate']
    ) * 0.2

    score += (
        signals['github_activity_score'] / 100
    ) * 0.2

    return score

#Combine Everything
