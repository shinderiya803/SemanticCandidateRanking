#Career History score
good_terms = ["retrieval", "ranking","recommendation","search","embedding",
              "vector","llm","fine-tuning","rag"]

bad_terms = ["speech","tts","asr","computer vision",
             "object detection","robotics"]

def career_score(candidate):

    text = ""

    for job in candidate['career_history']:
        text += " "
        text += job['title'].lower()
        text += " "
        text += job['description'].lower()

    score = 0

    for term in good_terms:
        if term in text:
            score += 1

    for term in bad_terms:
        if term in text:
            score -= 1

    score = max(score, 0)

    return min(score / len(good_terms), 1)

# Consulting Companies penalties
service_companies = ["tcs","infosys","wipro","cognizant","capgemini","accenture"]

def consulting_penalty(candidate):

    companies = [
        job['company'].lower()
        for job in candidate['career_history']
    ]

    count = 0

    for company in companies:
        if any(
            service in company
            for service in service_companies
        ):
            count += 1

    if count == len(companies):
        return 0.3

    return 0

#Improvised Final Score
